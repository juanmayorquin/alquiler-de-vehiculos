    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        Vehiculo vehiculoRelacionado = null;
        String placa = null;
        boolean placaExiste = false;

        if (!jTextField8.getText().isBlank() && jTextField8.getText().length() == 6) {
            placa = jTextField8.getText().toUpperCase();
            jTextField8.setBackground(Color.white);
        } else {
            JOptionPane.showMessageDialog(null, "El campo de placa debe contener 6 caracteres.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField8.setBackground(Color.red);
        }

        if (placa != null) {
            for (Alquiler a : listaAlquileres) {
                if (a.getVehiculo().getPlaca().equals(placa) && !a.getVehiculo().isDisponible()) {
                    a.setPagado(true);
                    vehiculoRelacionado = a.getVehiculo();
                    placaExiste = true;
                }
            }
        }
        
        if(vehiculoRelacionado != null){
            JOptionPane.showMessageDialog(null, "El vehículo " + vehiculoRelacionado.getMarca() + " " + vehiculoRelacionado.getColor() + " " + vehiculoRelacionado.getYear() + " fue entregado con éxito");
            
            for (Vehiculo v: listaVehiculos){
                if(v.getPlaca().equals(vehiculoRelacionado.getPlaca())){
                    v.setDisponible(true);
                }
            }
            
            vehiculoRelacionado.setDisponible(true);
            
        }else{
            JOptionPane.showMessageDialog(null, "El vehículo con esa placa no está disponible o no existe.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField8.setBackground(Color.red);
        }
        
        jTextField8.setBackground(Color.white);
        jTextField8.setText("");
    }//GEN-LAST:event_jButton10ActionPerformed

