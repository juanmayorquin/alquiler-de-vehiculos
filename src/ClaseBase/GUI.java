    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = null;
        String tipoDoc = null;
        int id = 0;
        boolean idExiste = false;

        if (!jTextField1.getText().isBlank()) {
            nombre = jTextField1.getText();
        } else {
            JOptionPane.showMessageDialog(null, "El campo de nombre debe contener algún caracter.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField1.setBackground(Color.red);
        }

        if (jComboBox2.getSelectedIndex() != 0) {
            tipoDoc = jComboBox2.getSelectedItem().toString();
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un tipo de documento.", "Error", JOptionPane.ERROR_MESSAGE);
            jComboBox2.setBackground(Color.red);
        }

        try {
            id = Integer.parseInt(jTextField2.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El número de documento debe ingresarse en enteros sin puntos, espacios ni comas.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField2.setBackground(Color.red);
        }

        if (nombre != null && tipoDoc != null) {
            Usuario usuario = new Usuario(nombre, tipoDoc, id);

            if (listaUsuarios.toArray().length > 0) {
                for (Usuario u : listaUsuarios) {
                    if (u.getId() == usuario.getId()) {
                        idExiste = true;
                    }
                }

                if (!idExiste) {
                    idExiste = false;
                    listaUsuarios.add(usuario);
                    JOptionPane.showMessageDialog(null, "Usuario añadido exitosamente.");
                } else {
                    JOptionPane.showMessageDialog(null, "Ya existe un usuario con esa identificación.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                listaUsuarios.add(usuario);
                JOptionPane.showMessageDialog(null, "Usuario añadido exitosamente.");
            }

            jTextField1.setBackground(Color.white);
            jComboBox2.setBackground(Color.white);
            jTextField2.setBackground(Color.white);

            jTextField1.setText("");
            jComboBox2.setSelectedIndex(0);
            jTextField2.setText("");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

