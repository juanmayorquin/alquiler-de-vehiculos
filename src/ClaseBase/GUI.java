    public void RecuperarDatos(LinkedList<Alquiler> listaAlquileres, LinkedList<Usuario> listaUsuarios, LinkedList<Vehiculo> listaVehiculos) {
        LinkedList<Alquiler> listaAlquileresRecuperada = new LinkedList<>();
        LinkedList<Usuario> listaUsuariosRecuperada = new LinkedList<>();
        LinkedList<Vehiculo> listaVehiculosRecuperada = new LinkedList<>();

        ObjectInputStream alquileres = null;
        ObjectInputStream usuarios = null;
        ObjectInputStream vehiculos = null;
        try {
            alquileres = new ObjectInputStream(new FileInputStream("alquileres.obj"));
            usuarios = new ObjectInputStream(new FileInputStream("usuarios.obj"));
            vehiculos = new ObjectInputStream(new FileInputStream("vehiculos.obj"));

            listaAlquileresRecuperada = (LinkedList<Alquiler>) alquileres.readObject();
            listaUsuariosRecuperada = (LinkedList<Usuario>) usuarios.readObject();
            listaVehiculosRecuperada = (LinkedList<Vehiculo>) vehiculos.readObject();

            // pasar los datos de laBD a bd
            listaAlquileres.addAll(listaAlquileresRecuperada);
            listaUsuarios.addAll(listaUsuariosRecuperada);
            listaVehiculos.addAll(listaVehiculosRecuperada);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error recuperando los datos " + e.getMessage() + " -- " + e.toString());
        } finally {
            try {
                alquileres.close();
                usuarios.close();
                vehiculos.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error cerrando ");
            }
        }
    }

    public void GuardarDatos(LinkedList<Alquiler> listaAlquileres, LinkedList<Usuario> listaUsuarios, LinkedList<Vehiculo> listaVehiculos) {
        ObjectOutputStream alquileres = null;
        ObjectOutputStream usuarios = null;
        ObjectOutputStream vehiculos = null;

        try {
            alquileres = new ObjectOutputStream(new FileOutputStream("alquileres.obj"));
            usuarios = new ObjectOutputStream(new FileOutputStream("usuarios.obj"));
            vehiculos = new ObjectOutputStream(new FileOutputStream("vehiculos.obj"));

            alquileres.writeObject(listaAlquileres);
            usuarios.writeObject(listaUsuarios);
            vehiculos.writeObject(listaVehiculos);
        } catch (Exception e) {
            System.out.println("Error almacenando los datos " + e.getMessage());
        } finally {
            try {
                alquileres.close();
                usuarios.close();
                vehiculos.close();
            } catch (Exception e) {
                System.out.println("Error cerrando");
            }
        }
    }

