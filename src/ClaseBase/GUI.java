    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int time = 0;
        int id = 0;
        String placa = null;

        Usuario usuarioParaAlquiler = null;
        Vehiculo vehiculoParaAlquiler = null;

        try {
            id = Integer.parseInt(jTextField4.getText());
            jTextField4.setBackground(Color.white);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El número de documento debe ingresarse en enteros sin puntos, espacios ni comas.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField4.setBackground(Color.red);
        }

        if (!jTextField5.getText().isBlank() && jTextField5.getText().length() == 6) {
            placa = jTextField5.getText().toUpperCase();
            jTextField5.setBackground(Color.white);
        } else {
            JOptionPane.showMessageDialog(null, "El campo de placa debe contener 6 caracteres.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField5.setBackground(Color.red);
        }

        try {
            time = Integer.parseInt(jTextField6.getText());
            jTextField6.setBackground(Color.white);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "La cantidad de días debe ingresarse en enteros sin puntos, espacios ni comas.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField6.setBackground(Color.red);
        }

        for (Usuario u : listaUsuarios) {
            if (u.getId() == id) {
                usuarioParaAlquiler = u;
            }
        }
        if (usuarioParaAlquiler == null) {
            JOptionPane.showMessageDialog(null, "No existe ningún usuario con ese número de identificación.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField4.setBackground(Color.red);
        }

        for (Vehiculo v : listaVehiculos) {
            if (placa.equals(v.getPlaca())) {
                vehiculoParaAlquiler = v;
            }
        }
        
        if (vehiculoParaAlquiler == null) {
            JOptionPane.showMessageDialog(null, "No existe ningún vehículo con esa placa.", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField5.setBackground(Color.red);
        }

        if (usuarioParaAlquiler != null && vehiculoParaAlquiler != null && time > 0) {
            Alquiler alquiler = new Alquiler(numReg, time, usuarioParaAlquiler, vehiculoParaAlquiler);            
            if (listaAlquileres.toArray().length > 0) {
                if (vehiculoParaAlquiler.isDisponible()) {
                    listaAlquileres.add(alquiler);
                    vehiculoParaAlquiler.setDisponible(false);
                    JOptionPane.showMessageDialog(null, "Alquiler añadido con éxito.");                    
                    numReg++;
                    
                } else {
                    JOptionPane.showMessageDialog(null, "El vehículo no está disponible.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                listaAlquileres.add(alquiler);
                JOptionPane.showMessageDialog(null, "Alquiler añadido con éxito.");
                vehiculoParaAlquiler.setDisponible(false);
                numReg++;
            }

            jTextField4.setBackground(Color.white);
            jTextField5.setBackground(Color.white);
            jTextField6.setBackground(Color.white);

            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

